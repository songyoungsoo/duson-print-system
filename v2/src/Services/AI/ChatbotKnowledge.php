- 크라프트지: 갈색 자연 질감. 친환경 스티커·패키지
- 은데드롱: 은색 금속 느낌. 고급 라벨·화장품 스티커
- 투명지: 투명 재질. 음료·화장품 라벨

● 평량(gsm) — 클수록 두꺼움
- 80~100g: 복사용지 / 120~150g: 전단지 / 200~250g: 명함·카다록 표지 / 300g+: 고급 명함·상품권

● 코팅
- 유광: 광택, 색상 선명 / 무광: 부드럽고 고급 / 라미네이팅: 방수·내구성 최고 / 비코팅: 필기·도장 가능

● 후가공
- 도무송(톰슨): 모양대로 오려내기. 초회 목형비 별도
- 오시: 접힘 홈. 두꺼운 종이(180g+) 필수
- 접지: 2단/3단/대문접지. 미싱: 점선 칼집(티켓·쿠폰)
- 박(금박/은박): 금속 필름 부착. 형압: 엠보싱(볼록)/디보싱(오목)
- 타공: 구멍 뚫기. 귀돌이: 모서리 둥글게

● 인쇄 도수
- 1도: 단색(저렴). 4도(CMYK): 풀컬러. "4도/4도"=양면컬러, "4도/1도"=앞컬러+뒤단색

● 합판 vs 독판
- 합판: 여러 주문 합쳐 인쇄, 저렴. 독판: 단독 인쇄, 색상 정밀, 가격 높음

